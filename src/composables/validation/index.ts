import { isEmpty } from "lodash"

export const validations = {
  mustBeFilled: function (value: unknown, message: string = 'Must be filled') {
    return !isEmpty(value) || message
  },

  mustMatchRegex: function (value: string = '', regex: RegExp, message: string = 'Value does not match given Regular Expression') {
    return regex.test(value) || message
  },

  mustBeEmail: function (value: string, message: string = 'Must be a valid email address') {
    const regex = /(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|"(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21\x23-\x5b\x5d-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])*")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\[(?:(?:(2(5[0-5]|[0-4][0-9])|1[0-9][0-9]|[1-9]?[0-9]))\.){3}(?:(2(5[0-5]|[0-4][0-9])|1[0-9][0-9]|[1-9]?[0-9])|[a-z0-9-]*[a-z0-9]:(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21-\x5a\x53-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])+)\])/
    return validations.mustMatchRegex(value, regex, message)
  }
}
